    public static UiLanguage DefaultLanguage =>
        CultureInfo.CurrentUICulture.TwoLetterISOLanguageName.Equals("zh", StringComparison.OrdinalIgnoreCase)
            ? UiLanguage.Chinese
            : UiLanguage.English;

    public static string Get(UiLanguage language, string key)
    {
        var table = language == UiLanguage.Chinese ? Chinese : English;
        return table.TryGetValue(key, out var value) ? value : key;
    }

    public static string Format(UiLanguage language, string key, params object?[] args)
    {
        return string.Format(CultureInfo.CurrentCulture, Get(language, key), args);
    }

    public static string State(UiLanguage language, TunnelRunState state)
    {
        return Get(language, $"State.{state}");
    }
